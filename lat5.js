function Ttambah(){
        var wadah1 = parseFloat(document.tambah.a1.value);
        var wadah2 = parseFloat(document.tambah.a2.value);
        var wadah3 = wadah1 + wadah2;
        
        alert("hasil tambah hasil: " + wadah3);
}

